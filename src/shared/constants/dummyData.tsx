export const formatTimestamp = (timestamp: any) => {
  if (!timestamp) return '';
  // Handle both number and string timestamps (Android uses Long which is number)
  const timestampNum = typeof timestamp === 'string' ? parseInt(timestamp, 10) : timestamp;
  const date = new Date(timestampNum);
  // Check if date is valid
  if (isNaN(date.getTime())) return '';
  const day = String(date.getDate()).padStart(2, '0'); // '01'
  const month = String(date.getMonth() + 1).padStart(2, '0'); // '03'
  const year = date.getFullYear(); // 2025
  return `${day}.${month}.${year}`;
};

// ðŸ§­ Distance calculation (Haversine formula)
export const getDistanceInKm = (
  lat1: number,
  lon1: number,
  lat2: number,
  lon2: number,
) => {
  const R = 6371; // radius of Earth in km
  const dLat = (lat2 - lat1) * (Math.PI / 180);
  const dLon = (lon2 - lon1) * (Math.PI / 180);
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * (Math.PI / 180)) *
      Math.cos(lat2 * (Math.PI / 180)) *
      Math.sin(dLon / 2) *
      Math.sin(dLon / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c; // distance in km
};
